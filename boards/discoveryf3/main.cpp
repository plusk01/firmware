#include "discoveryf3.h"
#include "rosflight.h"
#include "mavlink.h"

int main(void)
{
  rosflight_firmware::DiscoveryF3 board;
  rosflight_firmware::Mavlink mavlink(board);
  rosflight_firmware::ROSflight firmware(board, mavlink);

  board.init_board();
  firmware.init();

  while (true)
  {
    // delay(100);
    // board.led1_toggle();
    firmware.run();
  }
  return 0;
}
